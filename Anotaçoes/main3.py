# calculo de imc + f= formataçao
nome = "Pedro Henrique"
peso = 85
altura = 1.80
imc = peso / (altura * altura)
linha_1 = f"{nome} tem {peso} e {altura:.2f} de altura entao o seu imc é:"
linha_2 = f"{imc:.2f}"
print (nome, "tem 85 kg e 1.80 de altura então o seu imc é:", int (imc)) 
print(linha_1)
print (linha_2)
        